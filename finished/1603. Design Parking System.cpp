class ParkingSystem {
    int slots[3];
public:
    ParkingSystem(int big, int medium, int small) {
        ios_base::sync_with_stdio(0);
        cin.tie(0);
        cout.tie(0);
        slots[0] = big;
        slots[1] = medium;
        slots[2] = small;
    }

    bool addCar(int carType) {
        slots[carType - 1]--;
        return slots[carType - 1] >= 0;
    }
};