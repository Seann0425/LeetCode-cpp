class Solution {
public:
    int longestSubarray(vector<int> &nums) {
        const auto n = nums.size();
        auto ans = 0uz;
        auto zero_cnt = 0;
        for (size_t l = 0, r = 0; r < n; r++) {
            if (!nums[r]) zero_cnt++;
            if (zero_cnt == 1) ans = max(ans, r - l + 1);
            while (zero_cnt > 1)
                if (!nums[l++]) zero_cnt--;
        }
        return static_cast<int>(ans ? ans - 1 : n - 1);
    }
};