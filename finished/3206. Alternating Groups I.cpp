class Solution {
public:
    int numberOfAlternatingGroups(vector<int> &colors) {
        const auto n = colors.size();
        auto ans = 0;
        for (auto i = 1; i < n - 1; i++)
            if (colors[i] != colors[i - 1] and colors[i] != colors[i + 1]) ans++;
        if (colors[0] != colors[1] and colors[0] != colors.back()) ans++;
        if (colors[n - 1] != colors[n - 2] and colors[n - 1] != colors[0]) ans++;
        return ans;
    }
};